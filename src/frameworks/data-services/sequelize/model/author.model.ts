import { Model, Table, Column, DataType } from 'sequelize-typescript';

@Table({ tableName: 'authors' })
export class Author extends Model<Author> {
  @Column({
    type: DataType.INTEGER,
    primaryKey: true,
    autoIncrement: true,
  })
  id!: number;

  @Column({
    type: DataType.STRING,
    allowNull: false,
  })
  firstName!: string;

  @Column({
    type: DataType.STRING,
    allowNull: false,
  })
  lastName!: string;
}
